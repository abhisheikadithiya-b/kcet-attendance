import re
import json

students = []

def parse_raw(raw_str, class_name):
    for line in raw_str.strip().split('\n'):
        if not line.strip(): continue
        # Matches e.g., "1 25UAD001 ASHOK KUMAR A"
        match = re.match(r'^\d+\s+([A-Z0-9]+)\s+(.+)$', line.strip())
        if match:
            rollno = match.group(1).strip()
            name = match.group(2).strip()
            student_obj = {
                "id": f"std-{rollno.lower()}",
                "name": name,
                "studentId": rollno,
                "dept": "ADS",
                "year": class_name,
                "percent": 95
            }
            students.append(student_obj)

parse_raw(d11_raw, "d11")
parse_raw(d12_raw, "d12")

# Let's generate the javascript students block
js_block = "const students = " + json.dumps(students, indent=2) + ";"

# Read app.js
with open(r'assets/js/app.js', 'r', encoding='utf-8') as f:
    content = f.read()

# Find the students array declaration and replace it
# Matches const students = [ ... ];
pattern = r'const students = \[\s*[\s\S]*?\n\];'
modified_content = re.sub(pattern, js_block, content)

with open(r'assets/js/app.js', 'w', encoding='utf-8') as f:
    f.write(modified_content)

print(f"Imported {len(students)} students successfully!")
